<script type="text/javascript">
    $(function () {
        $('div#sidebar input.price-calc, div#sidebar input.num-class, div#sidebar input.num-pupil').on('keyup', function () {
            var price = $('div#sidebar input.price-calc').val();
            var numClass = $('div#sidebar input.num-class').val();
            var numStudent = $('div#sidebar input.num-pupil').val();

            if (!isNaN(price) && !isNaN(numClass) && !isNaN(numStudent)) {

                var total = parseFloat((((price * numClass) * numStudent) * 4));
                total = total - (total * 0.1476)

                $('div#sidebar span.earning-total').html(new Intl.NumberFormat('en-GB', {
                    style: 'currency',
                    currency: 'EUR'
                }).format(total))
            } else {
                $('div#sidebar span.earning-total').html('0.00')
            }

        })

